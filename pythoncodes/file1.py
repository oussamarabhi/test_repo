# This is a simple Python script that calculates the sum of two numbers.
# This is the first number.
num1 = 10

# This is the second number
num2 = 20

# Summation of the two numbers
result = num1 + num2

""" 
 This is a multiline comment that prints the result
of the summation. Here, we print the result to
the console and check if the result is greater
than 10. 
 """

if result > 10:
    print("The summation is greater that 10. The result is: ", result)
else:
    print("The summation is smaller than 10. The result is: ", result)